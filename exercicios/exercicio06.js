/*

Declare duas variáveis e atribua valores booleanos a elas. 
Use os operadores lógicos AND, OR e NOT para realizar operações lógicas entre as variáveis. Imprima os resultados.

*/

const variavel1 = true;
const variavel2 = false;
console.log(`${variavel1} AND ${variavel2} = ${variavel1 && variavel2}`);
console.log(`${variavel1} OR ${variavel2} = ${variavel1 || variavel2}`);
console.log(`NOT ${variavel1} = ${!variavel1}`);
console.log(`NOT ${variavel2} = ${!variavel2}`);